char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (*src != '\0')
	{
		dest[0] = src[0];
		src++;
		dest++;
		i++;
	}
	dest[0] = '\0';
	while (i-- != 0)
		dest--;
	return (dest);
}
